<script>
import { defineComponent } from "vue";
import { Link } from "@inertiajs/inertia-vue3";
import axios from "axios";
import { Inertia } from "@inertiajs/inertia";
import { toast } from "vue3-toastify";
export default defineComponent({
    props: ["sidebar", "isMobile"],
    data() {
        return {
            theme: 'system',
            notifications: [],
            isNotifLoading: false,
            notificationDrop: false,
            themeModeDrop: false,
            form: {},
            notificationQueue: []
        };
    },
    components: {
        Link,
    },
    watch: {
        theme(value, oldValue) {
            const html = document.getElementsByTagName('html')[0];
            const theme = window.localStorage.getItem('theme');
            if (!theme || theme === 'system') {
                html.setAttribute("data-theme", "system")
            } else {
                html.setAttribute("data-theme", theme)
            }
        },
        notificationQueue: {
            handler(value, oldValue) {
                if (this.notificationQueue?.[0]) {
                    const data = this.notificationQueue?.[0];
                    if (this.$page.props.user.id !== data.user_id) {
                        this.showWebNotification(data);
                        toast.success(data.message, {
                            position: 'bottom-right',
                            duration: 5000,
                            iconColor: '#ffffff',
                            iconSize: '504px',
                            padding: '100px',
                            onClose: () => {
                                this.dequeueNotification()
                            },
                        });
                    }
                    else {
                        this.showWebNotification(data);
                    }
                }
            },
            deep: true
        }
    },

    methods: {
        search() {
            this.isLoading = true;
            Inertia.get("/projects", this.form, {
                onFinish(response) {
                    this.isLoading = false;
                },
            });
        },

        logout() {
            Inertia.post(route("logout"));
        },
        getNotifications(value) {
            this.notificationDrop = value;
            axios.get("/notifications", { params: { limit: 10 } })
                .then((response) => {
                    this.notifications = response.data.data;
                })
                .finally(() => { this.isNotifLoading = false });
        },

        toggleThemeModeDrop(value) {
            this.themeModeDrop = value;
        },
        handleThemeChange(theme) {
            this.theme = theme;
            window.localStorage.setItem("theme", theme);
        },
        showWebNotification(data) {
            if ('Notification' in window) {
                Notification.requestPermission().then((permission) => {
                    if (permission === 'granted') {
                        const options = {
                            body: data.message,
                            icon: '/assets/images/ars.ico',

                        };
                        if (Notification.permission === 'granted') {
                            new Notification(data.title, options);
                        }
                    }
                });
            }
        },
        enqueueNotification(notification) {
            this.notificationQueue.push(notification);
        },
        dequeueNotification() {
            this.notificationQueue.shift();
        },
    },
    mounted() {
        // window.Echo.channel('notifications').listen('SendMessage', (event) => {
        //     console.log("This is event send message message", event)
        // });
        window.Echo.channel('notifications')
            .listen('NotificationEvent', (data) => {
                if (data.type == 'notification') {
                    this.enqueueNotification(data);
                }
            });
    },

    computed: {
        notificationCount() {
            return this.notifications.length;
        },
        currentNotification() {
            return this.notificationQueue[0];
        },
    },

});
</script>
